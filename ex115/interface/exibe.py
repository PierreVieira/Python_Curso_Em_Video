def multiplas_linhas():
    """
    Apenas printa vários tracinhos para deixar bonito
    :return: None
    """
    print('-'*50)


def ver_opcao(opcao):
    """
    Exibe a opção informada pelo usuário
    :param opcao: opção informada pelo usuário
    :return: None
    """
    multiplas_linhas()
    print(f'Opção {opcao}'.center(50))
    multiplas_linhas()